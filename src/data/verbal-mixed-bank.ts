export type VerbalQuestionCategoryId =
  | "analogy"
  | "sentence_completion"
  | "contextual_error"
  | "odd_word";

export type VerbalPracticeQuestion = {
  id: string;
  categoryId: VerbalQuestionCategoryId;
  prompt: string;
  options: string[];
  correctAnswer: string;
  explanation: string;
  source: string;
};

export type VerbalQuestionCategory = {
  id: VerbalQuestionCategoryId;
  title: string;
  description: string;
  href: string;
};

const ANALOGY_EXPLANATION =
  "لأن العلاقة بين كلمتي السؤال علاقة ترادف أو تقارب في المعنى، وهو نفس ما يحققه الخيار الصحيح.";

function makeAnalogyQuestion(
  id: string,
  prompt: string,
  options: string[],
  correctAnswer: string,
  source: string,
): VerbalPracticeQuestion {
  return {
    id,
    categoryId: "analogy",
    prompt,
    options,
    correctAnswer,
    explanation: ANALOGY_EXPLANATION,
    source,
  };
}

function makeSentenceCompletionQuestion(
  id: string,
  prompt: string,
  options: string[],
  correctAnswer: string,
  reason: string,
  source: string,
): VerbalPracticeQuestion {
  return {
    id,
    categoryId: "sentence_completion",
    prompt,
    options,
    correctAnswer,
    explanation: reason,
    source,
  };
}

function makeContextualErrorQuestion(
  id: string,
  prompt: string,
  options: string[],
  correctAnswer: string,
  reason: string,
  source: string,
): VerbalPracticeQuestion {
  return {
    id,
    categoryId: "contextual_error",
    prompt,
    options,
    correctAnswer,
    explanation: `الإجابة الصحيحة هي "${correctAnswer}" لأن ${reason}.`,
    source,
  };
}

function makeOddWordQuestion(
  id: string,
  prompt: string,
  options: string[],
  correctAnswer: string,
  reason: string,
  source: string,
): VerbalPracticeQuestion {
  return {
    id,
    categoryId: "odd_word",
    prompt,
    options,
    correctAnswer,
    explanation: `الإجابة الصحيحة هي "${correctAnswer}" لأن ${reason}.`,
    source,
  };
}

